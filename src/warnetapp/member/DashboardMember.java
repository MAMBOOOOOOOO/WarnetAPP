    @SuppressWarnings("unchecked")
    private void startTimer(){
           timer = new javax.swing.Timer(1000, e -> {
        if (sisaDetik > 0) {
            sisaDetik--;
            int jam = sisaDetik / 3600;
            int menit = (sisaDetik % 3600) / 60;
            int detik = sisaDetik % 60;
            lblTime.setText(String.format("%02d : %02d : %02d", jam, menit, detik));
            if (sisaDetik % 10 == 0) {
                simpanSisaWaktu();
            }
        } else {
            timer.stop();
            lblTime.setText("00 : 00 : 00");
            javax.swing.JOptionPane.showMessageDialog(
                this, "Waktu habis!", "Info",
                javax.swing.JOptionPane.WARNING_MESSAGE
            );
            MemberLogin logout = new warnetapp.member.MemberLogin();
            logout.setVisible(true);
            logout.setLocationRelativeTo(null);
            this.dispose();
            
        }
    });
    timer.start();
    }  
    
    private void loadSisaWaktu(){
        try {
            Connection conn = DBConnection.getConnection();
            PreparedStatement ps = conn.prepareStatement(
                "SELECT saldo_menit FROM member WHERE username = ?"
            );

            ps.setString(1, loggedUsername);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                sisaDetik = rs.getInt("saldo_menit") * 60;
            }
        } catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());
        }
    }
    
    private void simpanSisaWaktu(){
        try {
            Connection conn = DBConnection.getConnection();
            PreparedStatement ps = conn.prepareStatement(
                    "UPDATE member SET saldo_menit = ? WHERE username = ?"
            );
            ps.setInt(1, (int) Math.ceil(sisaDetik / 60.0));
            ps.setString(2,loggedUsername);
            
            ps.executeUpdate();
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }
    
