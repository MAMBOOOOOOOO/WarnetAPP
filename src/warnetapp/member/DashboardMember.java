    private void btnReedemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReedemActionPerformed
        // TODO add your handling code here:
        String code = txtCodeReedem.getText().trim();
        
        
       if (code.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this,
                "Masukkan kode redeem dulu!",
                "Peringatan",
                javax.swing.JOptionPane.WARNING_MESSAGE);
        return; 
       }
       
       try {
           Connection conn =  DBConnection.getConnection();
           
           PreparedStatement ps = conn.prepareStatement(
                   "SELECT * FROM redeem_code WHERE kode = ? AND status = 'unused'"
           );
           ps.setString(1,code);
           ResultSet rs = ps.executeQuery();
           
           if(rs.next()){
               int tambahMenit = rs.getInt("tambah_menit");
               PreparedStatement psUsed = conn.prepareStatement(
                       "UPDATE redeem_code SET status = 'used' WHERE kode = ?"
               );
               psUsed.setString(1,code);
               psUsed.executeUpdate();
               
               PreparedStatement psAdd = conn.prepareStatement(
                       "UPDATE member SET saldo_menit = saldo_menit + ? WHERE username = ?"
               );
                psAdd.setInt(1, tambahMenit);
                psAdd.setString(2, loggedUsername);
                psAdd.executeUpdate();
                
                sisaDetik += tambahMenit * 60;
                txtCodeReedem.setText("");
                
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Berhasil +" + (tambahMenit / 60) + "Jam di Tambahkan!",
                        "Redeem Berhasil!",
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);
           }else {
               javax.swing.JOptionPane.showMessageDialog(this,
                       "Kode tidak valid atau sudah di pakai",
                       "Gagal",
                       javax.swing.JOptionPane.ERROR_MESSAGE);               
           }
       }catch(SQLException e){
           System.out.println("Error : " + e.getMessage());
       }
    }//GEN-LAST:event_btnReedemActionPerformed

