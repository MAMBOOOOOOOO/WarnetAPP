    private void btnDeleteMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteMemberActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMember.getSelectedRow();
        
        if (selectedRow == -1 ){
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Pilih Member Yang mau di Hapus!",
                    "Peringatan",
                    javax.swing.JOptionPane.WARNING_MESSAGE
                    );
            return;
        }
        
        String username = tblMember.getValueAt(selectedRow, 1).toString();
        
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                 "Hapus member " + username + "?",
            "Konfirmasi Hapus",
        javax.swing.JOptionPane.YES_NO_OPTION);
                
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                Connection conn = DBConnection.getConnection();
                PreparedStatement ps = conn.prepareStatement("DELETE FROM member WHERE username = ?");
                ps.setString(1,username);
                ps.executeUpdate();
                
                loadMember("");
            }catch (SQLException e){
                System.out.println("Erorr" + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteMemberActionPerformed

