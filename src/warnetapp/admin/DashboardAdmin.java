    private void btnCreateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateAccountActionPerformed
        // TODO add your handling code here:
            String username = txtUser.getText().trim();
        String noHp = txtPhoneNumber.getText().trim();
        String password = "1";
        int saldoMenit = 600;

        // Validasi kosong

        if (username.isEmpty() && noHp.isEmpty()) {
            alertLabel.setForeground(java.awt.Color.RED);
            alertLabel.setText("*Username & Nomor HP tidak boleh kosong!");
            alertLabel.setVisible(true);
            return;
        }
        
        if (username.isEmpty()){
            alertLabel.setForeground(java.awt.Color.RED);
            alertLabel.setText("*Username tidak boleh kosong!");
            alertLabel.setVisible(true);
            return;
        }
        
        if (noHp.isEmpty()){
            alertLabel.setForeground(java.awt.Color.RED);
            alertLabel.setText("Nomor HP tidak boleh kosong!");
            alertLabel.setVisible(true);
            return;
        }
        // validasi mengandung spasi
        if (username.contains(" ")) {
            alertLabel.setForeground(java.awt.Color.RED);
            alertLabel.setText("*Username tidak boleh mengandung spasi!");
            alertLabel.setVisible(true);
            txtUser.setText("");
            return;
        }
        //Validasi ANgka
        if (!noHp.matches("[0-9]+")) {
            alertLabel.setForeground(java.awt.Color.RED);
            alertLabel.setText("*No HP tidak valid! Hanya boleh angka.");
            alertLabel.setVisible(true);
            txtPhoneNumber.setText("");
            return;
        }
        


        try {
            Connection conn = DBConnection.getConnection();
            String query = "INSERT INTO member (username, password, no_hp, saldo_menit) VALUES (?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, username);
            ps.setString(2, password);
            ps.setLong(3, Long.parseLong(noHp));
            ps.setInt(4, saldoMenit);

            ps.executeUpdate();

            alertLabel.setForeground(java.awt.Color.GREEN);
            alertLabel.setText("*Akun berhasil dibuat!");
            alertLabel.setVisible(true);
            txtUser.setText("");
            txtPhoneNumber.setText("");

        } catch (SQLException e) {
            if (e.getMessage().contains("no_hp")) {
                alertLabel.setText("*No HP sudah terdaftar!");
            } else {
                alertLabel.setText("*Username sudah dipakai!");
            }
            alertLabel.setForeground(java.awt.Color.RED);
            alertLabel.setVisible(true);
        }
    }//GEN-LAST:event_btnCreateAccountActionPerformed

