    private void loadMember(String keyword) {
        DefaultTableModel model = (DefaultTableModel) tblMember.getModel();
        model.setRowCount(0);

        try {
            Connection conn = DBConnection.getConnection();
            String query;
            PreparedStatement ps;

            if (keyword.isEmpty()) {
                // Tampilkan semua member
                query = "SELECT * FROM member";
                ps = conn.prepareStatement(query);
            } else {
                // Filter by username
                query = "SELECT * FROM member WHERE username LIKE ?";
                ps = conn.prepareStatement(query);
                ps.setString(1, "%" + keyword + "%");
            }

            ResultSet rs = ps.executeQuery();
            int no = 1;

            while (rs.next()) {
                int saldoMenit = rs.getInt("saldo_menit");
                int jam = saldoMenit / 60;
                int menit = saldoMenit % 60;
                String waktu = jam + " jam " + menit + " menit";

                model.addRow(new Object[]{
                    no++,
                    rs.getString("username"),
                    rs.getString("no_hp"),
                    waktu
                });
            }

        } catch (SQLException e) {
            System.out.println("Error: " + e.getMessage());
        }
    }    

    private String generateKode(){
        String chars = "ABCEDFGHIJKLMNOPQRSTUVWXYZ0123456789";
        Random random = new Random();
        StringBuilder code = new StringBuilder();
        for (int i = 0; i < 10; i++) {
            code.append(chars.charAt(random.nextInt(chars.length())));
        }
        return code.toString();
    }
    private void loadKode() {
        DefaultTableModel model = (DefaultTableModel) tblCode.getModel();
        model.setRowCount(0);
        
        try {
            Connection conn = DBConnection.getConnection();
            ResultSet rs = conn.prepareStatement("SELECT * FROM redeem_code").executeQuery();
            int no = 1;
            
            while (rs.next()) {
                int menit = rs.getInt("tambah_menit");
                String durasi = (menit / 60)+ "Jam";
                
                model.addRow(new Object[]{
                    no++,
                    rs.getString("kode"),
                    durasi,
                    rs.getString("status") 
                });
            }
        } catch (SQLException e) {
            System.out.println("Error: " + e.getMessage());
        }
    }
    
    
    
